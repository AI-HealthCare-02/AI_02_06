"""medicine_chunk.embedding: vector(3072) -> halfvec(3072) 타입 변환.

PLAN.md (feat/halfvec-hnsw-rag) §1 — pgvector 0.8.2 의 halfvec 타입은
HNSW 인덱스를 4,000 dimensions 까지 지원하므로 3072d 임베딩에 ANN 인덱스
적용 가능. 본 마이그는 ALTER COLUMN 만 처리하고, HNSW 인덱스는 트랜잭션
박싱 회피 + DML 동시 가능을 위해 자동배포 후 EC2 에서 별도 SQL 로
``CREATE INDEX CONCURRENTLY`` 수동 실행.

본 마이그레이션의 안전성 근거:
- 16-bit float 양자화는 cosine 유사도에서 실용상 무시 가능한 정밀도 손실
- 308,748 chunks × halfvec 변환은 pgvector 내장 cast 사용 (재임베딩 0)
- 변환 시간: 약 30~120초 (테이블 ACCESS EXCLUSIVE 잠금)

DDL 변경 요약:
1. embedding 컬럼 vector(3072) -> halfvec(3072)
   (pgvector cast 로 즉시 변환, 데이터 유지)

후속 운영 SQL (마이그 외, EC2 에서 수동 실행):
    CREATE INDEX CONCURRENTLY medicine_chunk_embedding_hnsw_idx
        ON medicine_chunk
        USING hnsw (embedding halfvec_cosine_ops)
        WITH (m = 16, ef_construction = 64);
"""

